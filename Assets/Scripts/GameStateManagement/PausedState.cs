public class PausedState : GameState
{
    public PausedState(GameStateMachine context) : base(context) {}
    public override void EnterState()
    {
        base.EnterState();
    }
    public override void ExitState()
    {
        base.ExitState();
    }
    public override void Tick()
    {
        base.Tick();
    }
    
}